// While Loop
var counterOne = 0;
while (counterOne < 10) {
  console.log(counterOne);
  counterOne++;
}

var counterOne = 10;
while (counterOne >= 1) {
  console.log(counterOne);
  counterOne--;
}

// Do While Loop - will run at least once. First it does, then checks the condition
var counterTwo = 10;
do{
  console.log(counterTwo);
  counterTwo--;
} while (counterTwo > 0);
